# ==========================================================
# Prompt Formatter
# ==========================================================

def build_prompt(template: str, context: dict[str, Any]) -> str:
    """
    Inject serialized context into a prompt template.
    """

    serialized_context = json.dumps(
        context,
        indent=2,
        default=str
    )
    
    return template.format(context = serialized_context, constraints = SYSTEM_CONSTRAINTS)